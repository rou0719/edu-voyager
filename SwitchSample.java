public class SwitchSample
{
    public static void main(String[] args)
    {
        int i =4;
      
        switch(i)
        {
            case 1:
                  
                System.out.println("iの値は1です");
            
                break;

            case 2:
                  
                System.out.println("iの値は2です");
            
                break;
                  
            case 3:
                  
                System.out.println("iの値は3です");
            
                break;
                
            case 4:
                  
                System.out.println("iの値は4です");
            
                break;
        }
    }
}    
