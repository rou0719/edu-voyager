public class SwitchSample3
{
    public static void main(String[] args)
    {
        char c = 'か';
      
        switch(c)
        {
            case 'あ':

                System.out.println("c の値は「あ」");
            
                break;

            case 'い':

                System.out.println("c の値は「い」");
            
                break;
                
            case 'う':

                System.out.println("c の値は「う」");
            
                break;

            case 'え':

                System.out.println("c の値は「え」");
            
                break;

            case 'お':

                System.out.println("c の値は「お」");
            
                break;
              
            default:

                System.out.println("iの値に当てはまらなかった");
            
        }
    }
}    
